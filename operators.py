#Bitwise Operator
a = 5
b = 10
print(a & b)
print(a | b)
print(~a)
print(a ^ b)
print(a << b)
print(a >> b)
# Identity Operator
x=[1,2,3,4]
y=x
z=[1,2,3,4]
print(x is y)
print(x is z)
print(x is not z)

# Arithmetic operator
a=10
b=3
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b)
print(a%b)
print(a**b)
# Comparision or Relational Operator
x=5
y=8
print(x==y)
print(x!=y)
print(x>=y)
print(x<=y)
# Logical Operator
#and
p = 5
q = 10
print(p>2 and q>5)
print(p>2 and q<5)
print(p<2 and q>5)
print(p<2 and q<5)
#or
r = 5
s = 10
print(r>2 or s>5)
print(r>2 or s<5)
print(r<2 or s>5)
print(r<2 or s<5)
#not
j = 4
k = 0
print(not(j))
print(not(k))
